import 'package:flutter/material.dart';
import 'package:spacebook_redux2_anim/values/values.dart';


// Style Gradient
const Gradient _gradient = Gradients.customgradient;

// Style Foreground Color
const Color _foregroundColor = AppColors.primaryText;


class CustomStyleDecoration extends BoxDecoration {
  const CustomStyleDecoration({
    Color color,
    Gradient gradient = _gradient,
    Border border,
    BorderRadiusGeometry borderRadius,
    List<BoxShadow> boxShadow,
  }) : super(
         color: color,
         border: border,
         borderRadius: borderRadius,
         boxShadow: boxShadow,
         gradient: gradient,
       );
  
  CustomStyleDecoration.withOverrides({
    Color color,
    Gradient gradient = _gradient,
    double borderWidth,
    Color borderColor,
    BorderRadiusGeometry borderRadius,
    Color shadowColor,
    Offset shadowOffset,
    double shadowRadius,
  }) : super(
         color: color,
         borderRadius: borderRadius,
         gradient: gradient,
         border: Border.all(width: borderWidth ?? 0.0, color: borderColor ?? const Color(0xFF000000)),
         boxShadow: [ BoxShadow(color: shadowColor ?? const Color(0x00000000), offset: shadowOffset ?? Offset.zero, blurRadius: shadowRadius ?? 0.0) ]
       );
}


class CustomStyleTextStyle extends TextStyle {
  const CustomStyleTextStyle({
    Color color = _foregroundColor,
    double fontSize,
    FontWeight fontWeight,
    String fontFamily,
    FontStyle fontStyle,
    double height,
    double letterSpacing,
  }) : super(
         color: color,
         fontSize: fontSize,
         fontWeight: fontWeight,
         fontFamily: fontFamily,
       );
}


class CustomStyle extends StatelessWidget {
  const CustomStyle({
    Key key,
    this.decoration = const CustomStyleDecoration(),
    this.child,
  }) : super(key: key);
  final CustomStyleDecoration decoration;
  final Widget child;
  
  @override
  Widget build(BuildContext context) {
  
    return DefaultTextStyle(
      style: const CustomStyleTextStyle(),
      child: DecoratedBox(
        decoration: this.decoration,
        child: this.child,
      ),
    );
  }
}


class CustomStyleButton extends StatelessWidget {
  
  const CustomStyleButton({
    Key key,
    this.color,
    this.border = const BorderSide(),
    this.borderRadius = const BorderRadius.all(Radius.circular(0)),
    this.padding,
    @required this.onPressed,
    @required this.child,
  }): super(key: key);
  
  final Color color;
  final BorderSide border;
  final BorderRadiusGeometry borderRadius;
  final EdgeInsets padding;
  final VoidCallback onPressed;
  final Widget child;
  
  @override
  Widget build(BuildContext context) {
  
    return FlatButton(
      color: this.color,
      shape: RoundedRectangleBorder(
        side: this.border,
        borderRadius: this.borderRadius,
      ),
      onPressed: this.onPressed,
      child: DefaultTextStyle(
        style: const CustomStyleTextStyle(),
        child: this.child,
      ),
    );
  }
}


class CustomStyleSwitch extends StatelessWidget {
  const CustomStyleSwitch({
    Key key,
    @required this.value,
    @required this.onChanged,
    this.activeColor = _foregroundColor,
  }): super(key: key);
  final bool value;
  final ValueChanged<bool> onChanged;
  final Color activeColor;
  
  @override
  Widget build(BuildContext context) {
  
    return Switch.adaptive(
      value: this.value,
      onChanged: this.onChanged,
      activeColor: this.activeColor,
    );
  }
}


class CustomStyleSlider extends StatelessWidget {
  const CustomStyleSlider({
    Key key,
    @required this.value,
    @required this.onChanged,
    this.inactiveColor,
    this.activeColor = _foregroundColor,
    this.min,
    this.max,
  }): super(key: key);
  final double value;
  final ValueChanged<double> onChanged;
  final Color inactiveColor;
  final Color activeColor;
  final double min;
  final double max;
  
  @override
  Widget build(BuildContext context) {
  
    return Slider(
      value: this.value,
      onChanged: this.onChanged,
      activeColor: this.activeColor,
    );
  }
}


class CustomStyleCircularProgressIndicator extends StatelessWidget {
  const CustomStyleCircularProgressIndicator({
    Key key,
    this.color = _foregroundColor,
  }): super(key: key);
